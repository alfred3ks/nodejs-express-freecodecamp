/*
Modulos en Node.js:
Un modulo es una funcionalidad organizada en uno o varios archivos JavaScript que puede ser reutilizado en una aplicacion.

Podemos importar la funcionalidad de un modulo a otro.
Ventajas:
- Evitamos repeticion del codigo.
- Reutilizar el codigo.
- Es mas facil encontrar y corregit bugs.
- Es mas facil modificiar el codigo.
- Es mas facil agregar nuevas funcionalidades.

Vamos a ver como crear un modulo:
Debemos importar donde lo vamos a usar con la intruccion require()
Debemos exportar la funcion con la instruccion export.
*/